const Txo = require('ethereumjs-tx');
var Tx = Txo.Transaction;   
const contractAddress = '0x16B49B83BD601af4d666ECEc83b5471470a07667';
const web3 = new Web3(new Web3.providers.HttpProvider("http://localhost:8545"));
const contract = new web3.eth.Contract(abi, contractAddress);

class SCO {
    
    async registerNewUser(address,key){
        
        const account = address;
        const privateKey = Buffer.from(key, 'hex');
        console.log(contract.methods);
    
        const _data = contract.methods.register().encodeABI();
        web3.eth.getTransactionCount(account).then(async nonce => {
            var rawTx = {
            nonce: nonce,
            gasPrice: `0x20000000000`,
            gasLimit: `0x27511`,
            to: contractAddress,
            value: 5000,
            data: _data
            }
    
            var tx = new Tx(rawTx);
            tx.sign(privateKey);
            var serializedTx = tx.serialize();
            web3.eth.sendSignedTransaction('0x' + serializedTx.toString('hex')).on('receipt', console.log);
            console.log(`User ${account} registered successfully on blockchain!`)
        })
    };
    
    async declareWinner(questionOwner,answerOwner,amount,key){
        const accountSender = questionOwner;
        const accountReceiver = answerOwner;
        const privateKey = Buffer.from(key, 'hex');
        console.log(contract.methods);
    
        const _data = contract.methods.transfer().encodeABI();
    
        web3.eth.getTransactionCount(accountSender).then(async nonce => {
            var rawTx = {
            nonce: nonce,
            gasPrice: `0x20000000000`,
            gasLimit: `0x27511`,
            to: accountReceiver,
            value: amount,
            data: _data
            }
            var tx = new Tx(rawTx);
            tx.sign(privateKey);
            var serializedTx = tx.serialize();
            web3.eth.sendSignedTransaction('0x' + serializedTx.toString('hex')).on('receipt', console.log);
            console.log(`Transfer Successful!`);
            console.log(`Sender:${questionOwner} Receiver: ${answerOwner} Amount: ${amount}`);
        })
    }
    
};
 
export default SCO;


    

  
 





